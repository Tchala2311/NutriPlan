/**
 * Builds an explicit hard-constraint block from the user's dietary restrictions
 * and allergen list. Returns a ready-to-inject prompt section with concrete
 * Russian prohibition text. Returns empty string if no restrictions apply.
 */
export function buildHardConstraintsBlock(restrictions: string[], allergens: string[]): string {
  const lines: string[] = [];

  for (const r of restrictions) {
    const key = r.toLowerCase().trim();
    const prohibition = RESTRICTION_PROHIBITIONS[key];
    if (prohibition) lines.push(prohibition);
  }

  if (allergens.length > 0) {
    lines.push(
      `ЖЁСТКОЕ ИСКЛЮЧЕНИЕ (аллергены — НИКОГДА не использовать): ${allergens.join(', ')}. Наличие любого из этих продуктов в блюде недопустимо.`
    );
  }

  if (lines.length === 0) return '';

  return [
    '',
    '⚠️ ЖЁСТКИЕ ОГРАНИЧЕНИЯ РАЦИОНА — АБСОЛЮТНЫЕ ТРЕБОВАНИЯ (не рекомендации):',
    ...lines.map((l) => `- ${l}`),
    'Нарушение любого из этих ограничений недопустимо. Каждое блюдо и каждый ингредиент должны строго соответствовать перечисленным запретам.',
    '',
  ].join('\n');
}

// ── Goal-aware phase guidance helper ─────────────────────────────────────────

export function getPhaseGuidance(
  goal: string,
  phaseNumber: number,
  eatingDisorderFlag: boolean
): string {
  if (eatingDisorderFlag) {
    const edBank = PHASE_GUIDANCE_ED_SAFE[goal] ?? PHASE_GUIDANCE_ED_SAFE.general_wellness;
    return edBank[phaseNumber] ?? edBank[1];
  }
  const goalGuidance = PHASE_GUIDANCE[goal] ?? PHASE_GUIDANCE.general_wellness;
  return goalGuidance[phaseNumber] ?? goalGuidance[1];
}

// ── Goal-aware calorie target helper ─────────────────────────────────────────

export function getPhaseCalorieTarget(goal: string, phaseNumber: number): PhaseCalorieTarget {
  const overrides = PHASE_CALORIE_OVERRIDES[goal] ?? PHASE_CALORIE_OVERRIDES.general_wellness;
  return overrides[phaseNumber] ?? overrides[1];
}

