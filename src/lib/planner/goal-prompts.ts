// ── getMealPlanPrompt ─────────────────────────────────────────────────────────

export interface MealPlanPromptParams {
  primaryGoal: string;
  tdeeKcal: number;
  targetProteinG: number;
  targetCarbsG: number;
  targetFatG: number;
  dietaryRestrictions: string[];
  allergens: string[];
  avoidedIngredients: string[];
  medicalConditions: string[];
  eatingDisorderFlag: boolean;
  weekStart: string;
  weekEnd: string;
  phaseNumber: number;
  phaseName: string;
}

/**
 * Selects the correct goal-specific prompt, applies cross-cutting scenario
 * modifiers, and fills all placeholders.
 *
 * Scenario detection order:
 *  1. keto + weight_loss
 *  2. vegan/vegetarian + muscle_gain
 *  3. diabetes_t2, hypertension, ibs, pcos, hypothyroidism (any goal)
 *  4. eatingDisorderFlag (always last — overrides language in earlier sections)
 */
export function getMealPlanPrompt(params: MealPlanPromptParams): string {
  const goal = PHASE_CALORIE_OVERRIDES[params.primaryGoal]
    ? params.primaryGoal
    : "general_wellness";

  const basePrompt = PROMPT_MEAL_PLAN_BY_GOAL[goal] ?? PROMPT_MEAL_PLAN_BY_GOAL.general_wellness;
  const phaseGuidance = getPhaseGuidance(goal, params.phaseNumber, params.eatingDisorderFlag);
  const calorieTarget = getPhaseCalorieTarget(goal, params.phaseNumber);

  const deficitKcal = goal === "weight_loss"
    ? Math.max(0, params.tdeeKcal - calorieTarget.rest)
    : 0;
  const surplusKcal = goal === "muscle_gain"
    ? Math.max(0, calorieTarget.training - params.tdeeKcal)
    : 0;

  // Collect scenario modifiers
  const restrictions = params.dietaryRestrictions ?? [];
  const conditions = params.medicalConditions ?? [];
  const modifiers: string[] = [];

  if (restrictions.includes("keto") && goal === "weight_loss") {
    modifiers.push(SCENARIO_MODIFIERS.keto_weight_loss);
  }
  if (goal === "muscle_gain") {
    if (restrictions.includes("vegan")) {
      modifiers.push(SCENARIO_MODIFIERS.vegan_muscle);
    } else if (restrictions.includes("vegetarian")) {
      modifiers.push(SCENARIO_MODIFIERS.vegetarian_muscle);
    }
  }
  if (conditions.includes("diabetes_t2"))    modifiers.push(SCENARIO_MODIFIERS.diabetes_t2);
  if (conditions.includes("hypertension"))   modifiers.push(SCENARIO_MODIFIERS.hypertension);
  if (conditions.includes("ibs"))            modifiers.push(SCENARIO_MODIFIERS.ibs);
  if (conditions.includes("pcos"))           modifiers.push(SCENARIO_MODIFIERS.pcos);
  if (conditions.includes("hypothyroidism")) modifiers.push(SCENARIO_MODIFIERS.hypothyroidism);
  // Always last — overrides language in all preceding sections
  if (params.eatingDisorderFlag)             modifiers.push(SCENARIO_MODIFIERS.eating_disorder);

  const scenarioBlock = modifiers.length > 0
    ? modifiers.join("\n\n")
    : "";

  let prompt = basePrompt
    .replace("{{user.primary_goal}}", goal)
    .replace("{{user.tdee_kcal}}", String(params.tdeeKcal))
    .replace("{{user.target_protein_g}}", String(params.targetProteinG))
    .replace("{{user.target_carbs_g}}", String(params.targetCarbsG))
    .replace("{{user.target_fat_g}}", String(params.targetFatG))
    .replace("{{user.dietary_restrictions}}", restrictions.join(", ") || "нет")
    .replace("{{user.allergens}}", params.allergens.join(", ") || "нет")
    .replace("{{user.avoided_ingredients}}", params.avoidedIngredients.join(", ") || "нет")
    .replace("{{user.medical_conditions}}", conditions.join(", ") || "нет")
    .replace("{{phase_number}}", String(params.phaseNumber))
    .replace("{{phase_name}}", params.phaseName)
    .replace("{{phase_guidance}}", phaseGuidance)
    .replace("{{kcal_training}}", String(calorieTarget.training))
    .replace("{{kcal_rest}}", String(calorieTarget.rest))
    .replace("{{deficit_kcal}}", String(deficitKcal))
    .replace("{{surplus_kcal}}", String(surplusKcal))
    .replace("{{week_start}}", params.weekStart)
    .replace("{{week_end}}", params.weekEnd)
    .replace("{{scenario_modifiers}}", scenarioBlock)
    .replace("{{base_format}}", PROMPT_BASE_FORMAT);

  // ED-safe post-processing: scrub any residual calorie numbers from prompt text
  if (params.eatingDisorderFlag) {
    prompt = prompt
      .replace(/\b\d{3,4}\s*ккал\b/g, "достаточно калорий")
      .replace(/дефицит[^.]*ккал[^.]*\./gi, "умеренное питание.")
      .replace(/профицит[^.]*ккал[^.]*\./gi, "питательный рацион.")
      .replace(/TDEE\s*\d{3,4}/g, "TDEE пользователя");
  }

  return prompt;
}
