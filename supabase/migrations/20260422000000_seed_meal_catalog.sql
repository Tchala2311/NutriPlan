-- ── meals ─────────────────────────────────────────────────────────────────────
-- Static meal catalog for the NutriPlan 8-week program.
-- Not user-specific. Read-only for all authenticated users.
CREATE TABLE IF NOT EXISTS public.meals (
  id          UUID        PRIMARY KEY DEFAULT gen_random_uuid(),
  phase       INT         NOT NULL CHECK (phase IN (1, 2)),
  week        INT         NOT NULL CHECK (week BETWEEN 1 AND 8),
  day         INT         NOT NULL CHECK (day BETWEEN 0 AND 6),
  meal_type   TEXT        NOT NULL CHECK (meal_type IN ('breakfast','lunch','snack','dinner')),
  name        TEXT        NOT NULL,
  description TEXT,
  kcal        INT,
  protein_g   NUMERIC(5,1),
  carbs_g     NUMERIC(5,1),
  fat_g       NUMERIC(5,1),
  is_batch    BOOLEAN     NOT NULL DEFAULT FALSE
);

ALTER TABLE public.meals ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Authenticated users can read meals"
  ON public.meals FOR SELECT
  USING (auth.role() = 'authenticated');

-- ── shopping_items ────────────────────────────────────────────────────────────
-- Shopping list catalog for 8-week program.
-- window: A = days 1-3, B = days 4-7 within each week.
CREATE TABLE IF NOT EXISTS public.shopping_items (
  id                  UUID    PRIMARY KEY DEFAULT gen_random_uuid(),
  phase               INT     NOT NULL CHECK (phase IN (1, 2)),
  week                INT     NOT NULL CHECK (week BETWEEN 1 AND 8),
  shopping_window     CHAR(1) NOT NULL CHECK (shopping_window IN ('A','B')),
  category            TEXT    NOT NULL,
  category_order      INT     NOT NULL DEFAULT 0,
  item_name           TEXT    NOT NULL,
  quantity_per_person TEXT
);

ALTER TABLE public.shopping_items ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Authenticated users can read shopping items"
  ON public.shopping_items FOR SELECT
  USING (auth.role() = 'authenticated');

